package cursobasicojava;

public class Aula13OperadoresRelacionais {

	public static void main(String[] args) {
		//relacionais
				// == igual 
				// != diferente de 
				//> maior que
				//< menor que
				//>= maior ou igual que
				//<= menor ou igual que
				
				int valor1=1;
				int valor2=2;

				System.out.println("valor1 == valor2: " + (valor1==valor2));
				System.out.println("valor1 != valor2: " + (valor1!= valor2));
				System.out.println("valor1 > valor2: " + (valor1>valor2));
				System.out.println("valor1 => valor2: " + (valor1>=valor2));
				System.out.println("valor1 < valor2: " + (valor1<valor2));
				System.out.println("valor1 <= valor2: " + (valor1<=valor2));
	}
	}
