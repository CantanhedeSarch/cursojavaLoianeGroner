public class Aula17LoopForNormal {

	public static void main(String[] args) {
		// Executa um bloco de codigo que esta dentro do comando for enquanto uma expressão for verdadeira
		//exemplo do for "normal"
		// variaveis declarada dentro do bloco de codigo for, são usadas apenas para for, em caso de uso da variavel fora do bloco for, da erro.
		
		for(int i=0; i<5;i++) {
			System.out.println("i tem valor de:  "+i);
		}
		
		for(int i=5; i>0;i--) {
			System.out.println("i tem valor de:  "+i);
		}
		
	}

}
